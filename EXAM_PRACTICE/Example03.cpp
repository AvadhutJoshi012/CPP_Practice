#include <iostream>
using namespace std;

int main()
{
    cout << "Program for Typecasting" << endl;

    int a = 9;
    cout << a << endl;
    float b = a;
    cout << b << endl;
    float c = (float)a;
    cout << c << endl;
    cout << a / 2 << endl;
    cout << b / 2 << endl;
    cout << c / 2 << endl;
}